    bool checkCuda(cudaError_t err, const char* operation)
        {
            if (err == cudaSuccess) {
                return true;
            }

            qWarning() << "CUDA error during" << operation << ":"
                    << cudaGetErrorString(err) << "(" << static_cast<int>(err) << ")";

            if (err == cudaErrorNoKernelImageForDevice || err == cudaErrorInvalidDeviceFunction) {
                qWarning() << "CUDA kernel binary is not compatible with this GPU."
                           << "Check NVCC gencode/architecture flags in the .pro file.";
            }
            return false;
        }
}

// CUDA constant memory for lookup tables
__constant__ int d_edgeTable[256];
__constant__ int d_triTable[256][16];

// CUDA device function for vertex interpolation
__device__ float3 interpolateVertexDevice(float isoLevel, int x, int y, int z, float v1, float v2, int edge) {
    if (v1 == v2) return make_float3(x, y, z);

    float t = (isoLevel - v1) / (v2 - v1);
    t = fminf(fmaxf(t, 0.0f), 1.0f);

    float3 vertex;
    switch (edge) {
    case 0:  vertex = make_float3(x + t, y, z); break;
    case 1:  vertex = make_float3(x + 1, y + t, z); break;
    case 2:  vertex = make_float3(x + 1 - t, y + 1, z); break;
    case 3:  vertex = make_float3(x, y + 1 - t, z); break;
    case 4:  vertex = make_float3(x + t, y, z + 1); break;
    case 5:  vertex = make_float3(x + 1, y + t, z + 1); break;
    case 6:  vertex = make_float3(x + 1 - t, y + 1, z + 1); break;
    case 7:  vertex = make_float3(x, y + 1 - t, z + 1); break;
    case 8:  vertex = make_float3(x, y, z + t); break;
    case 9:  vertex = make_float3(x + 1, y, z + t); break;
    case 10: vertex = make_float3(x + 1, y + 1, z + t); break;
    case 11: vertex = make_float3(x, y + 1, z + t); break;
    default: vertex = make_float3(x, y, z); break;
    }

    return vertex;
}

// CUDA kernel for Marching Cubes algorithm
__global__ void marchingCubesKernel(
    float* volume,
    float isoLevel,
    int nx, int ny, int nz,
    float3* vertices,
    int* triangleCount,
    int maxVertices
) {
    // Compute thread indices using blockIdx, blockDim, and threadIdx
    int x = blockIdx.x * blockDim.x + threadIdx.x;
    int y = blockIdx.y * blockDim.y + threadIdx.y;
    int z = blockIdx.z * blockDim.z + threadIdx.z;

    // Skip threads outside the volume bounds
    if (x >= nx - 1 || y >= ny - 1 || z >= nz - 1) {
        return;
    }

    // Get voxel values for the current cube (8 corners)
    int idx000 = z * ny * nx + y * nx + x;
    int idx100 = z * ny * nx + y * nx + (x + 1);
    int idx110 = z * ny * nx + (y + 1) * nx + (x + 1);
    int idx010 = z * ny * nx + (y + 1) * nx + x;
    int idx001 = (z + 1) * ny * nx + y * nx + x;
    int idx101 = (z + 1) * ny * nx + y * nx + (x + 1);
    int idx111 = (z + 1) * ny * nx + (y + 1) * nx + (x + 1);
    int idx011 = (z + 1) * ny * nx + (y + 1) * nx + x;

    float vals[8] = {
        volume[idx000],
        volume[idx100],
        volume[idx110],
        volume[idx010],
        volume[idx001],
        volume[idx101],
        volume[idx111],
        volume[idx011]
    };

    // Calculate cubeIndex by checking each voxel value against isoLevel
    int cubeIndex = 0;
    for (int i = 0; i < 8; i++) {
        if (vals[i] < isoLevel) {
            cubeIndex |= (1 << i);
        }
    }

    // Skip empty or full cubes
    if (cubeIndex == 0 || cubeIndex == 255) {
        return;
    }

    // Get edges from the edgeTable lookup
    int edges = d_edgeTable[cubeIndex];

    // Interpolate vertices along edges
    float3 vertList[12];
    if (edges & 1)    vertList[0] = interpolateVertexDevice(isoLevel, x, y, z, vals[0], vals[1], 0);
    if (edges & 2)    vertList[1] = interpolateVertexDevice(isoLevel, x + 1, y, z, vals[1], vals[2], 1);
    if (edges & 4)    vertList[2] = interpolateVertexDevice(isoLevel, x + 1, y + 1, z, vals[2], vals[3], 2);
    if (edges & 8)    vertList[3] = interpolateVertexDevice(isoLevel, x, y + 1, z, vals[3], vals[0], 3);
    if (edges & 16)   vertList[4] = interpolateVertexDevice(isoLevel, x, y, z + 1, vals[4], vals[5], 4);
    if (edges & 32)   vertList[5] = interpolateVertexDevice(isoLevel, x + 1, y, z + 1, vals[5], vals[6], 5);
    if (edges & 64)   vertList[6] = interpolateVertexDevice(isoLevel, x + 1, y + 1, z + 1, vals[6], vals[7], 6);
    if (edges & 128)  vertList[7] = interpolateVertexDevice(isoLevel, x, y + 1, z + 1, vals[7], vals[4], 7);
    if (edges & 256)  vertList[8] = interpolateVertexDevice(isoLevel, x, y, z, vals[0], vals[4], 8);
    if (edges & 512)  vertList[9] = interpolateVertexDevice(isoLevel, x + 1, y, z, vals[1], vals[5], 9);
    if (edges & 1024) vertList[10] = interpolateVertexDevice(isoLevel, x + 1, y + 1, z, vals[2], vals[6], 10);
    if (edges & 2048) vertList[11] = interpolateVertexDevice(isoLevel, x, y + 1, z, vals[3], vals[7], 11);

    // Generate triangles from the triTable lookup
    for (int i = 0; d_triTable[cubeIndex][i] != -1 && i < 15; i += 3) {
        int base = atomicAdd(triangleCount, 3);

        // Prevent out-of-bounds writes when output buffer is capped by available VRAM.
        if (base + 2 >= maxVertices) {
            return;
        }
        
        // Store interpolated vertices in output array
        vertices[base] = vertList[d_triTable[cubeIndex][i]];
        vertices[base + 1] = vertList[d_triTable[cubeIndex][i + 1]];
        vertices[base + 2] = vertList[d_triTable[cubeIndex][i + 2]];
    }
}

