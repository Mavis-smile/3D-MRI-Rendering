    }

    // (g) Copy triangle count back to host
    if (!checkCuda(cudaMemcpy(&h_triangleCount, d_triangleCount, sizeof(int), cudaMemcpyDeviceToHost),
                   "cudaMemcpy(D2H triangleCount)")) {
        goto cleanup;
    }

    if (h_triangleCount <= 0) {
        qWarning() << "Marching Cubes produced zero vertices."
                   << "Try adjusting threshold/iso level.";
        goto cleanup;
    }

    copyVertexCount = h_triangleCount;
    if (copyVertexCount > maxVertices) {
        qWarning() << "Vertex output truncated by GPU memory limit."
                   << "Produced:" << h_triangleCount << "Capped to:" << maxVertices;
        copyVertexCount = maxVertices;
    }

    // (g) Copy generated mesh vertices back to host
    h_vertices = new float3[copyVertexCount];
    if (!checkCuda(cudaMemcpy(h_vertices, d_vertices, static_cast<size_t>(copyVertexCount) * sizeof(float3), cudaMemcpyDeviceToHost),
                   "cudaMemcpy(D2H vertices)")) {
        goto cleanup;
    }

    // Convert results to Mesh format for OpenGL rendering
    mesh.vertices.reserve(copyVertexCount);
    
    for (int i = 0; i < copyVertexCount; i++) {
        mesh.vertices.append(QVector3D(
            h_vertices[i].x * spacingX,
            h_vertices[i].y * spacingY,
            h_vertices[i].z * spacingZ
        ));
    }

    // Generate sequential indices for triangle rendering
    mesh.indices.reserve(copyVertexCount);
    for (int i = 0; i < copyVertexCount; i++) {
        mesh.indices.append(i);
    }

    // Debugging logs
    qDebug() << "Generated mesh:"
             << mesh.vertices.size() << "vertices,"
             << mesh.indices.size() / 3 << "triangles";

cleanup:
    delete[] h_volume;
    delete[] h_vertices;

    if (d_volume) {
        cudaFree(d_volume);
    }
    if (d_vertices) {
        cudaFree(d_vertices);
    }
    if (d_triangleCount) {
        cudaFree(d_triangleCount);
    }

    return mesh;
}

MarchingCubes::Mesh MarchingCubes::generateMeshStreaming(
    const QVector<QVector<QVector<float>>>& volume,
    float isoLevel,
    int slabDepth,
    float spacingX,
    float spacingY,
    float spacingZ
    ) {

    Mesh merged;
    if (volume.isEmpty()) {
        qWarning() << "generateMeshStreaming: volume is empty";
        return merged;
    }

    const int depth = volume.size();
    if (depth < 2 || volume[0].isEmpty() || volume[0][0].isEmpty()) {
        qWarning() << "generateMeshStreaming: volume too small for Marching Cubes";
        return merged;
    }

    const int effectiveSlabDepth = qMax(2, slabDepth);
    if (depth <= effectiveSlabDepth) {
        return generateMesh(volume, isoLevel, spacingX, spacingY, spacingZ);
    }

    qDebug() << "Streaming Marching Cubes with slab depth" << effectiveSlabDepth
             << "on full volume depth" << depth;

    int zStart = 0;
    while (zStart < depth - 1) {
        const int zEnd = qMin(depth - 1, zStart + effectiveSlabDepth - 1);
        const int localDepth = zEnd - zStart + 1;

        QVector<QVector<QVector<float>>> slab;
        slab.resize(localDepth);
        for (int z = 0; z < localDepth; ++z) {
            slab[z] = volume[zStart + z];
        }

        Mesh slabMesh = generateMesh(slab, isoLevel, spacingX, spacingY, spacingZ);
        if (!slabMesh.vertices.isEmpty()) {
            const unsigned int baseIndex = static_cast<unsigned int>(merged.vertices.size());

            merged.vertices.reserve(merged.vertices.size() + slabMesh.vertices.size());
            for (const QVector3D& v : slabMesh.vertices) {
                merged.vertices.append(QVector3D(v.x(), v.y(), v.z() + (static_cast<float>(zStart) * spacingZ)));
            }

            merged.indices.reserve(merged.indices.size() + slabMesh.indices.size());
            for (unsigned int idx : slabMesh.indices) {
                merged.indices.append(baseIndex + idx);
            }
        }

        qDebug() << "Processed slab z" << zStart << "to" << zEnd
                 << "(" << (zEnd - zStart + 1) << "slices )";

        if (zEnd == depth - 1) {
            break;
        }

        // Overlap by one slice so boundary cubes are preserved without skipping source data.
        zStart = zEnd;
    }

    qDebug() << "Streaming mesh complete:" << merged.vertices.size()
             << "vertices," << merged.indices.size() / 3 << "triangles";
    return merged;
}
